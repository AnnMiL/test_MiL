function y = H11(x)

if x <= 0
    y = Inf;
    return
end

p1 =     0.01114  ;
       p2 =     -0.4183  ;
       p3 =       3.335  ;
       p4 =       1.557  ;
       p5 =   -0.005141  ;
       p6 =    0.001241  ;
       q1 =     -0.9893  ;
       q2 =       4.732  ;
       q3 =      0.2124  ;
       q4 =    0.004207  ;
       q5 =   0.0001899  ;

       
y =    (p1*x.^5 + p2*x.^4 + p3*x.^3 + p4*x.^2 + p5*x + p6) ./...
               (x.^5 + q1*x.^4 + q2*x.^3 + q3*x.^2 + q4*x + q5);

       